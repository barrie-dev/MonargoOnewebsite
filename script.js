function setLanguage(lang) {
  const selected = translations[lang] || translations.nl;
  document.documentElement.lang = lang;
  localStorage.setItem('monargo-language', lang);
  document.querySelectorAll('[data-i18n]').forEach(element => {
    const key = element.dataset.i18n;
    if (selected[key]) element.textContent = selected[key];
  });
  document.querySelectorAll('[data-lang]').forEach(button => button.classList.toggle('active', button.dataset.lang === lang));
}

document.querySelectorAll('[data-lang]').forEach(button => button.addEventListener('click', () => setLanguage(button.dataset.lang)));
setLanguage(localStorage.getItem('monargo-language') || 'nl');

const menuToggle = document.querySelector('.menu-toggle');
const navLinks = document.querySelector('.nav-links');
if (menuToggle && navLinks) {
  menuToggle.addEventListener('click', () => {
    navLinks.classList.toggle('open');
    menuToggle.setAttribute('aria-expanded', navLinks.classList.contains('open'));
  });
  navLinks.querySelectorAll('a').forEach(link => link.addEventListener('click', () => navLinks.classList.remove('open')));
}

document.querySelectorAll('.usecase-tab').forEach(button => {
  button.addEventListener('click', () => {
    const target = button.dataset.target;
    document.querySelectorAll('.usecase-tab').forEach(item => item.classList.remove('active'));
    document.querySelectorAll('.usecase-panel').forEach(item => item.classList.remove('active'));
    button.classList.add('active');
    document.getElementById(target)?.classList.add('active');
  });
});

const revealObserver = new IntersectionObserver(entries => {
  entries.forEach(entry => {
    if (entry.isIntersecting) {
      entry.target.classList.add('visible');
      revealObserver.unobserve(entry.target);
    }
  });
}, { threshold: .12 });
document.querySelectorAll('.reveal').forEach(element => revealObserver.observe(element));

document.querySelectorAll('form[data-contact-form]').forEach(form => {
  form.addEventListener('submit', async event => {
    event.preventDefault();
    const submit = form.querySelector('[type="submit"]');
    const status = form.querySelector('[data-form-status]');
    submit.disabled = true;
    status.textContent = 'Bezig met verzenden...';
    try {
      const response = await fetch('/api/contact', { method: 'POST', headers: { 'Content-Type': 'application/json' }, body: JSON.stringify(Object.fromEntries(new FormData(form))) });
      if (!response.ok) throw new Error('Verzenden mislukt');
      form.reset();
      status.textContent = 'Bedankt. We nemen snel contact met je op.';
    } catch (error) {
      status.textContent = 'Het formulier kon niet worden verzonden. Mail ons via hello@monargo.one.';
    } finally {
      submit.disabled = false;
    }
  });
});
